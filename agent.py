def create_commit(repo):
    print("Creating branch and commit...")

    branch = repo.create_head("ai-fix-branch")
    repo.head.reference = branch
    repo.head.reset(index=True, working_tree=True)

    repo.git.add(A=True)
    repo.index.commit("AI generated fix")

    repo.remote().push(branch)


def create_pull_request():

    url = f"https://api.github.com/repos/{index.REPO_OWNER}/{index.REPO_NAME}/pulls"

    data = {
        "title": "AI Automated Fix",
        "head": "ai-fix-branch",
        "base": index.BASE_BRANCH,
        "body": "This pull request was automatically generated by an AI agent."
    }

    headers = {
        "Authorization": f"token {index.GITHUB_TOKEN}"
    }

    response = requests.post(url, json=data, headers=headers)

    print("Pull request created:", response.json())


