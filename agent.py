import os
import subprocess
import requests
from git import Repo
from openai import OpenAI

import index

client = OpenAI(api_key=index.OPENAI_API_KEY)


def clone_repository():
    if not os.path.exists("repo"):
        print("Cloning repository...")
        Repo.clone_from(index.REPO_URL, "repo")
    return Repo("repo")


def run_pylint():
    print("Running pylint...")
    result = subprocess.run(
        ["pylint", "repo"],
        capture_output=True,
        text=True
    )
    return result.stdout


def generate_fix(issue_text):
    print("Generating fix using AI...")

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {
                "role": "system",
                "content": "You are a Python expert. Fix the code issues."
            },
            {
                "role": "user",
                "content": issue_text
            }
        ]
    )

    return response.choices[0].message.content


